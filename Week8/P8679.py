prev = [[None]*50 for _ in range(30)]

Dirs = [('D', 1, 0), ('U', -1, 0), ('L', 0, -1), ('R', 0, 1)]

from collections import deque
x, y = 0, 0
q = deque()
q.append((0,0))
visited[x][y] = True

while q:
    x, y = q.popleft()
    if x == 29 and y == 49:
        break
    for dir, dx, dy in Dirs:
        nx, ny = x+dx, y+dy
        if nx >= 0 and ny >= 0 and nx <= 29 and ny <= 49 and visited[nx][ny] == False and maze[nx][ny] == '0':
            visited[nx][ny] = True
            prev[nx][ny] = (dir, x, y)
            q.append((nx, ny))

